package edu.ucsb.aelmore.util;

public class NonUniqueVoteServerException extends VoteServerException {

	public NonUniqueVoteServerException() {
	}

	public NonUniqueVoteServerException(String arg0) {
		super(arg0);
	}

	public NonUniqueVoteServerException(Throwable arg0) {
		super(arg0);
	}

	public NonUniqueVoteServerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
